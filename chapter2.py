# ==========================================
# 2.1 变量 — 就是给数据起个名字
# ==========================================

message = "Hello, Python 世界!"
print(message)      # 输出变量的值

# 变量的值可以改
message = "我改了变量的值"
print(message)

# 变量命名规则：
# ✅ 只能用字母、数字、下划线，不能数字开头
# ✅ 区分大小写：Name 和 name 是两个变量
# ❌ 不能叫 print、class 等 Python 关键字

# ==========================================
# 2.2 字符串 — 就是文本
# ==========================================

name = "ming ming"          # 双引号
greeting = '你好'            # 单引号也行
both = "He said, '你好'"    # 引号嵌套

# 字符串大小写变换
print(name.title())         # 首字母大写 → Ming Ming
print(name.upper())         # 全大写 → MING MING
print(name.lower())         # 全小写 → ming ming

# ==========================================
# 2.3 在字符串里插入变量（f-string）
# ==========================================

first_name = "ming"
last_name = "wang"
full_name = f"{first_name} {last_name}"    # f 开头，花括号里放变量
print(f"你好，{full_name.title()}!")       # Hello, Ming Wang!

# ==========================================
# 2.4 数字 — 整数和浮点数
# ==========================================

age = 25
price = 19.99
print(f"我今年 {age} 岁")                  # f-string 里可以放数字
print(age + 5)      # 加法 → 30
print(age - 3)      # 减法 → 22
print(age * 2)      # 乘法 → 50
print(age / 2)      # 除法 → 12.5（注意结果是浮点数）
print(age ** 2)     # 乘方 → 25² = 625

# 数字转字符串（要拼到文本里时需要）
print("我今年 " + str(age) + " 岁")       # str() 把数字变字符串

