# 第5章：if 判断 — 让代码做选择

# ==========================================
# 5.1 最简单的 if：条件成立才执行
# ==========================================

age = 18

if age >= 18:
    print("你可以进网吧了")
# age=18，18>=18 成立 → 打印

if age < 18:
    print("你还不能进网吧")
# age=18，18<18 不成立 → 跳过，啥也不发生

# ==========================================
# 5.2 if-else：二选一
# ==========================================

temperature = 35

if temperature > 30:
    print("太热了，开空调")      # 条件成立走这里
else:
    print("还行，不用开")        # 条件不成立走这里

# ==========================================
# 5.3 if-elif-else：多选一
# ==========================================

score = 85

if score >= 90:
    print("优秀")
elif score >= 80:               # elif = 如果上面不成立，再试试这个
    print("良好")               # ← 85 走到这里就停了
elif score >= 60:
    print("及格")
else:                           # 上面全不成立，走这里
    print("不及格")

# 注意：从上往下检查，哪个先满足就走哪个，后面的不看了

# ==========================================
# 5.4 比较符号全家桶
# ==========================================

x = 10

print(x == 10)     # 等于 → True
print(x != 5)      # 不等于 → True
print(x > 5)       # 大于 → True
print(x < 20)      # 小于 → True
print(x >= 10)     # 大于等于 → True
print(x <= 9)      # 小于等于 → False

# 多个条件
print(x > 5 and x < 20)    # 两个都成立 → True
print(x > 5 or x < 1)      # 一个成立就行 → True
print(not x > 100)         # 取反 → True

# ==========================================
# 5.5 in / not in — 检查在不在列表里
# ==========================================

foods = ["汉堡", "披萨", "炸鸡", "薯条"]

print("披萨" in foods)       # 披萨在列表里吗？→ True
print("寿司" in foods)       # 寿司在列表里吗？→ False

if "披萨" in foods:
    print("今天有披萨！")

if "寿司" not in foods:
    print("今天没有寿司")

