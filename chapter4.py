# 第4章：操作列表 — 切片循环、复制、组元
# ==========================================
# 4.1 遍历切片（for + 切片一起用）
# ==========================================

fruits = ["苹果", "西瓜", "菠萝", "芒果", "梨"]

# 只遍历前3个
for fruit in fruits[:3]:
    print(fruit)
# 输出：苹果、西瓜、菠萝

# 遍历后3个
for fruit in fruits[-3:]:
    print(fruit)
# 输出：菠萝、芒果、梨

# ==========================================
# 4.2 复制列表（这是个经典坑！）
# ==========================================

original = [1, 2, 3]

# ❌ 错！这不是复制，是贴标签
bad_copy = original
bad_copy.append(999)
print(original)      # [1, 2, 3, 999] ← original 也被改了！

# ✅ 对！用切片复制
real_copy = original[:]
real_copy.append(888)
print(original)      # [1, 2, 3, 999] ← original 没动
print(real_copy)     # [1, 2, 3, 999, 888]

# 为什么？看内存：
# bad_copy = original    → bad_copy 和 original 指向同一块数据，改一个全改
# real_copy = original[:] → 新建一块独立的数据，各管各的

# ==========================================
# 4.3 元组（tuple）— 不能修改的列表
# ==========================================

# 圆括号，不是方括号
colors = ("红", "绿", "蓝")

print(colors[0])       # 红  — 取值跟列表一样
print(colors[-1])      # 蓝

# colors[0] = "黄"     # ❌ 报错！元组不能改

# 元组虽然不能改，但可以整个重写
colors = ("黄", "紫", "白")
print(colors)          # ('黄', '紫', '白')

