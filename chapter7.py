# 练习2：用 while + input 做一个猜数字游戏
#        设定答案 answer = 7，用户输入猜的数字，猜对提示正确并退出
#        猜错提示大了还是小了
answer = 7
while True:
    number = int(input("猜一个数字 "  ))  #int 可以把字符串 转换成数字 没有int 是"7" != 7
    if number == 7:
        print("猜测正确")
        break
    elif answer < number:
        print("猜大了,重新猜")
    elif answer > number:
        print("猜小了,重新猜")




# 练习3：创建一个待办列表，用 while 逐个 pop 处理，
#        每处理一个打印处理了什么，直到列表为空
exercise =["波比跳","深蹲","胯下击掌","俯卧撑","登山跑"]
while exercise :
    exercises = exercise.pop()
    print(f"处理了{exercises}")

print ("处理完成")
